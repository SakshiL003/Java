package com.feb22;

public class DoWhileLoop {

	public static void main(String[] args) {
		//Increment
		//prints no 1 to 10
		int i=1;
		do {
			System.out.println(i);
			i++;
			
		}while(i<=10);
		
		
		
		//Decrement
		//prints no 10 to 1
		int a=10;
		do {
			System.out.println(a);
			a--;
			
		}while(a>=1);

	}

}
